function idade(ano) {
  return 2025 - ano;
}
console.log(
  "Olá, meu nome é Elder, e eu nasci em 1939, minha idade é:",
  idade(1939)
);

function combustivel(litro) {
  return litro * 12;
}
var litros = 20;
console.log(
  "Eu abasteci meu carro com",
  litros,
  "litros, e agora consigo viajar",
  combustivel(litros),
  "km!"
);
